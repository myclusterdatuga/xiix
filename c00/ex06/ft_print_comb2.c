#include <unistd.h>
#include <stdbool.h>

void	ft_putchar(int c)
{
	write(1, &c, 1);
}

void	ft_prinrt_caracter(int a, int b)
{
	ft_putchar(48 + a / 10);
	ft_putchar(48 + a % 10);
	ft_putchar(' ');
	ft_putchar(48 + b / 10);
	ft_putchar(48 + b % 10);
	ft_putchar(' ');
	ft_putchar(',');
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			ft_prinrt_caracter(a, b);
			b++;
		}
		a++;
	}
}

int	main(void)
{
	ft_print_comb2();
}
